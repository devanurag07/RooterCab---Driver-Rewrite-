class UserProfile {
  final String id;
  final String fullName;
  final String phone;
  final String email;
  final String profile;
  final String gender;
  final String profileImage;

  UserProfile({
    required this.id,
    required this.fullName,
    required this.phone,
    required this.email,
    required this.profile,
    required this.gender,
    required this.profileImage,
  });
}
